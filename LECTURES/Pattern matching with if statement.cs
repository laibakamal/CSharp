using System;
using System.Collections.Generic;
using System.Text;

namespace EAD_Lecture_6
{
    class IterationStatements
    {
        static void Main(string[] args)
        {

            //while
            int xk = 0;
            while (xk < 10)
            {
                Console.WriteLine(xk);
                xk++;
            }
            /////////////////
            //do while
            string password = String.Empty;
            do
            {
                Console.Write("Enter your password: ");
                password = Console.ReadLine();
            }
            while (password != "123");
            Console.WriteLine("Correct!");
            ////////////
            ///for loop

            for (int y = 1; y <= 10; y++)
            {
                Console.WriteLine(y);
            }
            /////////////
            ///foreach

            string[] names = { "Adam", "Barry", "Charlie", "xyz" };
            foreach (string x in names)
            {
                Console.WriteLine($"{x} has {x.Length} characters.");
            }
        }
    }
}
