//Operators
/*
1 - unary operators(Increment, decrement, typeof, sizeof)
2 - binary operators(+, -, *,/)
3 - Assignment operators(=, +=, -=, *=, /=)
4 - Logical operators(AND, OR, XOR)
*/

using System;
using System.Collections.Generic;
using System.Text;

namespace EAD_Lec5
{
    class Operators
    {

        static void Main(string[] args)
        {
            Console.WriteLine("--Operators in C#--");

            //unary operators
            int a = 5;
            int b = a--; //postfix operator

            Console.WriteLine($"a is {a} and b is {b}");

            int c = 5;
            int d = --c; //prefix operator

            Console.WriteLine($"c is {c} and d is {d}");

            Console.WriteLine(sizeof(int));
            Console.WriteLine(typeof(int));



            ////binary operators
            int e = 6;
            int f = 3;

            Console.WriteLine($"e is {e} and f is {f}");

            Console.WriteLine($"e + f is {e + f}");
            Console.WriteLine($"e - f is {e - f}");
            Console.WriteLine($"e * f is {e * f}");
            Console.WriteLine($"e / f is {e / f}");


            ////assignment operators

            int p = 5;
            p += 3; //equivalent to p = p + 3;
            p -= 3; //equivalent to p = p - 3;
            p *= 3; //equivalent to p = p * 3;
            p /= 3; //equivalent to p = p / 3;


            ////logical operators
            ////these operators operate on boolean values so either return true or false.
            bool x = true;
            bool y = false;

            ////AND  both should be true
            Console.WriteLine("----------AND-----------");
            Console.WriteLine(x & x);
            Console.WriteLine(x & y);
            Console.WriteLine(y & x);
            Console.WriteLine(y & y);

            ////OR  //either one operator can be true
            Console.WriteLine("----------OR-----------");
            Console.WriteLine(x | x);
            Console.WriteLine(x | y);
            Console.WriteLine(y | x);
            Console.WriteLine(y | y);


            ////XOR (Exclusive OR) either operator can be true but not both
            Console.WriteLine("----------XOR-----------");
            Console.WriteLine(x ^ x);
            Console.WriteLine(x ^ y);
            Console.WriteLine(y ^ x);
            Console.WriteLine(y ^ y);

        }
    }
}


